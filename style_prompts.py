STYLE_NAMES = {
    "business_casual": "📧 Email коллеге",
    "team_chat": "💬 Сообщение в чат",
    "documentation": "📝 Документация",
    "formal": "✉️ Официальное письмо",
    "basic": "✏️ Аккуратно отредактировать"
}


STYLE_DESCRIPTIONS = {
    "business_casual": "Профессиональный email для коллег и партнёров",
    "team_chat": "Структурированное сообщение для команды",
    "documentation": "Техническая документация и инструкции",
    "formal": "Строго официальное письмо",
    "basic": "Минимальные правки с сохранением стиля"
}


def get_style_prompt(style_key: str) -> str:
    """Get style prompt by key, returns default if not found."""
    return STYLE_PROMPTS.get(style_key, STYLE_PROMPTS["business_casual"])


def get_style_name(style_key: str) -> str:
    """Get human-readable style name."""
    return STYLE_NAMES.get(style_key, STYLE_NAMES["business_casual"])


def get_style_description(style_key: str) -> str:
    """Get style description."""
    return STYLE_DESCRIPTIONS.get(style_key, STYLE_DESCRIPTIONS["business_casual"])


def get_all_styles() -> list:
    """Get list of all available style keys."""
    return list(STYLE_PROMPTS.keys())
